#!/bin/bash

display_celtic_cross()
{
      echo -e "\e[93m"
      cat << "EOF"
      |V|
   .::| |::.
  ::__| |__::
 >____   ____<
  ::  | |  ::
   '::| |::'
      | |
      | |
      |A|

EOF
      echo -e "\e[0m"
}

display_left_sided_cross()
{
      echo -e "\e[93m"
      cat << "EOF"
     __
    /_/\/\
    \_\  /
    /_/  \
    \_\/\ \
       \_\/

EOF
      echo -e "\e[0m"
}

